    private void updatedetailsbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatedetailsbtnActionPerformed
        Boolean flag = true;
        int personId = Integer.parseInt(idtxt.getText());
        String name = nametxt.getText();
        if(name.matches("[a-zA-Z ]+") == false){
            JOptionPane.showMessageDialog(null, "Incorrect Name");
            flag = false;
            nametxt.setText("");
        }
        String email = emailtxt.getText();
        if(email.matches("[a-zA-Z0-9.@]+[gmail|northeastern][.com|.edu]+") == false){
            JOptionPane.showMessageDialog(null, "Incorrect Email");
            flag = false;
            emailtxt.setText("");
        }
        int age = Integer.parseInt(agetxt.getText());
        if(age < 0 && age > 100){
            JOptionPane.showMessageDialog(null, "Incorrect Age");
            flag = false;
            agetxt.setText("");
        }
        String region = regiontxt.getText();
        if(region.matches("[a-zA-Z, ]+") == false){
            JOptionPane.showMessageDialog(null, "Incorrect Address");
            flag = false;
            regiontxt.setText("");
        }
        
        String phno = phonetxt.getText();
        if(phno.matches("[0-9]{10}") == false){
            JOptionPane.showMessageDialog(null, "Incorrect PhoneNumber");
            flag = false;
            phonetxt.setText("");
        }
        String language = languagetxt.getText();
        int researchPaper = Integer.parseInt(paperpublishtxt.getText());
        int experience = Integer.parseInt(experiencetxt.getText());
        
        Boolean updatePersonStatus = manageperson.updatePerson(personId, name, age, region, Long.parseLong(phno), email);
        Boolean updateProfStatus = manageprofessor.updateProfessor(personId, language, researchPaper, experience);
        if(flag==true && updatePersonStatus==true && updateProfStatus==true){
            professor.setName(name);
            professor.setAge(age);
            professor.setRegion(region);
            professor.setPhone(Long.parseLong(phno));
            professor.setEmail(email);
            professor.setLangOfInstruction(language);
            professor.setResearchPaperPublished(researchPaper);
            professor.setYearsOfExperience(experience);
            JOptionPane.showMessageDialog(null, "Professor details updated successfully!");     
        }
        else{
            JOptionPane.showMessageDialog(null, "Error while updating Professor details. Check the information before submitting!");
        }

    }//GEN-LAST:event_updatedetailsbtnActionPerformed

    private void updatepassbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatepassbtnActionPerformed
        Boolean flag = true;
        int personId = Integer.parseInt(idtxt.getText());
        String prevPassword =new String(prevpasstxt.getPassword());
        String password = new String(newpasstxt.getPassword());
        String confirmpassword = new String(confirmpasstxt.getPassword());
        if(prevPassword.equals(password)){
            JOptionPane.showMessageDialog(null, "Previous password and new password cannot be same!");
            newpasstxt.setText("");
            confirmpasstxt.setText("");
            flag = false;
        }
        if(password.matches("[a-zA-Z0-9@._-]{3,}") == false){
            JOptionPane.showMessageDialog(null, "Incorrect Password Format");
            newpasstxt.setText("");
            confirmpasstxt.setText("");
            flag = false;
        }
        
        if(!password.equals(confirmpassword)){
            JOptionPane.showMessageDialog(null, "Confirm password doesnot match with set Password");
            confirmpasstxt.setText("");
            flag = false;
        }
        Boolean checkExistingPassStatus = manageperson.checkExistingPass(personId, prevPassword);
        if(checkExistingPassStatus==false){
            JOptionPane.showMessageDialog(null, "Incorrect Previous Password!");
            prevpasstxt.setText("");
            flag = false;
        }
//        System.out.println(prevPassword +"--"+password+"--"+confirmpassword);
        if(flag == true){
            Boolean updatePassStatus = manageperson.updatePassword(personId, password);
            if(updatePassStatus){
                JOptionPane.showMessageDialog(null, "Password updated Successfully");     
            }
            else{
                JOptionPane.showMessageDialog(null, "Error encountered while updating password. Try again!");     
            }   
        }
        else{
            JOptionPane.showMessageDialog(null, "Re-enter correct details to update password.");
        }
    }//GEN-LAST:event_updatepassbtnActionPerformed

